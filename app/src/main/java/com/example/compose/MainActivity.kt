@Composable
fun Greeting(name: String) {
    Text(
        text = "Hello $name!",
        fontSize = 32.sp,
        fontWeight = FontWeight.Bold,
        color = Color.Red,
        textAlign = TextAlign.Center,
        modifier = Modifier
            .background(color = Color.Yellow)
            .border(2.dp, color = Color.Green) //這個如果放在padding後面會緊靠文字
            .padding(10.dp)
            .fillMaxWidth()
            .fillMaxHeight(0.5f) //可以調整比例
    )
}

